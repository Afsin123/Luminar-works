// Construct a pyramid pyramid pattern
// 1
// 12
// 123
// 1234


for (let i=1; i<=4 ; i++)
{
    var str="";
    for (let j=1; j<=i; j++)
    {
        str+=j;
        
    }
    console.log(str);
}

